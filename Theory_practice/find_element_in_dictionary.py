d = {'1': 'one', '3': 'three', '2': 'two', '5': 'five', '4': 'four'}
print("is 1 in dictionary's keys:", "1" in d.keys())

d = {'1': 'one', '3': 'three', '2': 'two', '5': 'five', '4': 'four'}
